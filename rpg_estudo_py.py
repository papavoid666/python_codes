def combate_ragall_goblin_minion():
  while ragall.esta_vivo() and goblin_minion.esta_vivo():
      print("\n--- NOVO TURNO ---")
      print(f"{ragall.nome}: {ragall.vida} de vida")
      print(f"{goblin_minion.nome}: {goblin_minion.vida} de vida")

      acao = input("Escolha (a) atacar ou (f) fugir: ").lower()
      if acao == "a":
          ragall.atacar(goblin_minion)
          if goblin_minion.esta_vivo():
              goblin_minion.atacar(ragall)
      elif acao == "f":
          print("Você fugiu da batalha!")
          break
      else:
          print("Comando inválido.")
  if not ragall.esta_vivo():
      print("Você foi derrotado...")
  elif not goblin_minion.esta_vivo():
      print("Você venceu!")

