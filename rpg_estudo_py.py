#molde de personagens
class Personagem:
  def __init__(self,nome,vida,ataque):
    self.nome = nome
    self.vida = vida
    self.ataque = ataque
    self.nivel = 1

  def atacar(self,inimigo):
    print(f"{self.nome} ataca {inimigo.nome} causando {self.ataque} de dano!")
    inimigo.vida -= self.ataque

  def esta_vivo(self):
        return self.vida > 0

  def subir_nivel(self):
        self.nivel += 1
        aumento_vida = 10
        aumento_ataque = 2
        self.vida += aumento_vida
        self.ataque += aumento_ataque
        print(f"{self.nome} subiu para o nível {self.nivel}!")
        print(f"Vida aumentou para {self.vida}, ataque agora é {self.ataque}.")

#personagens
